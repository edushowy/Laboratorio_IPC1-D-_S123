package generico;

/**
 *
 * @author edush
 */
public class Elemento {
    private String nombre;
    private int nota;

    public Elemento() {
        nombre="";
        nota=0;
    }

    public Elemento(String nombre, int nota) {
        this.nombre = nombre;
        this.nota = nota;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getNota() {
        return nota;
    }

    public void setNota(int nota) {
        this.nota = nota;
    }
    
    
    
}
